int	ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb < 1)
		return (0);
	if (nb > 46340 * 46340)
		return (0);
	while (i * i <= nb)
	{
		if (nb == i * i)
			return (i);
		i++;
	}
	return (0);
}
